package Assignment_1;
import java.time.LocalDate;

public class exam {
	private int id;
	private String code;
	private String title;
	private categoryQuestion categoryQuestion;
	private int Duration;
	private account creator;
	private LocalDate createDate;
	public exam(int id, String code, String title, categoryQuestion categoryQuestion, int duration, account creator,
			LocalDate createDate) {
		super();
		this.id = id;
		this.code = code;
		this.title = title;
		this.categoryQuestion = categoryQuestion;
		Duration = duration;
		this.creator = creator;
		this.createDate = createDate;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public categoryQuestion getCategoryQuestion() {
		return categoryQuestion;
	}
	public void setCategoryQuestion(categoryQuestion categoryQuestion) {
		this.categoryQuestion = categoryQuestion;
	}
	public int getDuration() {
		return Duration;
	}
	public void setDuration(int duration) {
		Duration = duration;
	}
	public account getCreator() {
		return creator;
	}
	public void setCreator(account creator) {
		this.creator = creator;
	}
	public LocalDate getCreateDate() {
		return createDate;
	}
	public void setCreateDate(LocalDate createDate) {
		this.createDate = createDate;
	}
	public String toString() {
		return "Exam [ id = " + id + ", code = " + code + ", title = " + title + ", categoryQuestion = " + categoryQuestion
				+ ", Duration = " + Duration + ", creator=" + creator + ", createDate = " + createDate + " ]";
	}
}
